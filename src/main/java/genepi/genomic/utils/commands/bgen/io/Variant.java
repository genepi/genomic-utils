package genepi.genomic.utils.commands.bgen.io;

public class Variant {

	private String contig;

	private int start;

	public String getContig() {
		return contig;
	}

	public void setContig(String contig) {
		this.contig = contig;
	}

	public int getStart() {
		return start;
	}

	public void setStart(int start) {
		this.start = start;
	}

}
